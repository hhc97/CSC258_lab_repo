def bin_rep(num, lst=None):
    if not lst:
        lst = ['0', '1']
    if num == 1:
        return lst
    else:
        new_lst = []
        for item in lst:
            new_lst.append(item + '0')
            new_lst.append(item + '1')
        return bin_rep(num - 1, new_lst)


def separator(st):
    lst = []
    for digit in st:
        lst.append(digit)
    return lst


